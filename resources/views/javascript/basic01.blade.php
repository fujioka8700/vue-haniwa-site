        ワラビー (wallaby) は、フクロネズミ目（有袋類）カンガルー科に属する動物のうち、カンガルーやワラルーよりも小さな種に対し、一般的に使われる名称である。特に明確な定義付けはなされていないものの、約30種いる。カンガルーに比べ、後ろ足が小さく尾が短い。しかし、後ろ足で跳躍し移動すること、育児嚢で子供を育てることなど、基本的な習性はカンガルーと同じである。
