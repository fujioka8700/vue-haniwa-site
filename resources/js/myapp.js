// Switchステートメント
const key = 555;
switch (key) {
    case 1:
        document.write("Pay by Visa Card");
        break;
    case 2:
        document.write("Pay by Master Card");
        break;
    case 3:
        document.write("Pay by Paypal");
        break;
    default:
        document.write("Pay by face to face");
        break;
}