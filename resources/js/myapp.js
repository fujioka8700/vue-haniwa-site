// 関数, パラメータ, 戻り値
function add(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function division(a, b) {
    return a / b;
}

let result = add(4, 2);
document.write(result);

document.write("<br>");

result = sub(4, 2);
document.write(result);

document.write("<br>");

result = multiply(4, 2);
document.write(result);

document.write("<br>");

result = division(4, 2);
document.write(result);